package chapter03;

public class Operate14 {

	public static void main(String[] args) {
		
		int num = 10;
		
		num+=5; //num= num+5와 같다.
		System.out.println(num);
		num-=5;
		System.out.println(num);
		num*=2;
		System.out.println(num);
		num/=2;
		System.out.println(num);
		num%=3;
		System.out.println(num);

	}

}
